fn sub_word(w: u32) -> u32 {
    let (b0, b1, b2, b3) = be_unword!(w);
    be_word!(S_BOX[b0 as uint], S_BOX[b1 as uint], S_BOX[b2 as uint], S_BOX[b3 as uint])
}

fn rot_word(w: u32) -> u32 {
    rotr!(w, 8)
}

